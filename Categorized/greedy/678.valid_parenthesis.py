class Solution:
    def checkValidString(self, s: str) -> bool:
        leftmin, leftmax = 0, 0

        for char in s:
            if char == "(":
                leftmin += 1
                leftmax += 1
            elif char == ")":
                leftmin -= 1
                leftmax -= 1
            else:
                leftmin -= 1
                leftmax += 1

            if leftmin < 0: # Never to go negative, (*)(
                leftmin = 0
            if leftmax < 0: # )(
                return False
        return leftmin == 0